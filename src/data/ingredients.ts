export function searchIngredients(query: string): IngredientData[] {
  const q = query.toLowerCase().trim();
  if (!q) return [];

  const startsWithResults: IngredientData[] = [];
  const includesResults: IngredientData[] = [];

  for (const item of INGREDIENTS) {
    const nameMatch = item.name.startsWith(q);
    const includesMatch = !nameMatch && item.name.includes(q);
    const aliasStartsWith =
      !nameMatch && item.aliases?.some((a) => a.startsWith(q));
    const aliasIncludes =
      !nameMatch && !includesMatch && !aliasStartsWith && item.aliases?.some((a) => a.includes(q));

    if (nameMatch || aliasStartsWith) {
      startsWithResults.push(item);
    } else if (includesMatch || aliasIncludes) {
      includesResults.push(item);
    }
  }

  return [...startsWithResults, ...includesResults].slice(0, 8);
}
